                            <a class="badge badge-secondary" href="https://www.dndbeyond.com/sources/phb/combat#BreakingUpYourMove" title="View description on D&amp;D Beyond" target="_BLANK">
                                Breaking Up Your Move
                            </a>
                        </td>
                        <td>
                            <p>You can break up your movement on your turn, using some of your speed before and after your action. For example, if you have a speed of 30 feet, you can move 10 feet, take your action, and then move 20 feet.</p>

                            <p class="font-italic">Moving Between Attacks</h3>

                            <p>If you take an action that includes more than one weapon attack, you can break up your movement even further by moving between those attacks. For example, a fighter who can make two attacks with the Extra Attack feature and who has a speed of 25 feet could move 10 feet, make an attack, move 15 feet, and then attack again.</p>

                            <p class="font-italic">Using Different Speeds</h3>

                            <p>If you have more than one speed, such as your walking speed and a flying speed, you can switch back and forth between your speeds during your move. Whenever you switch, subtract the distance you've already moved from the new speed. The result determines how much farther you can move. If the result is 0 or less, you can't use the new speed during the current move.</p>
                        </td>
                    </tr>
                    <tr>
                        <td>
                            <a class="badge badge-secondary" href="https://www.dndbeyond.com/sources/phb/combat#DifficultTerrain" title="View description on D&amp;D Beyond" target="_BLANK">
                                Difficult Terrain
                            </a>
                        </td>
                        <td>
                            <p>Every foot of movement in difficult terrain costs 1 extra foot. This rule is true even if multiple things in a space count as difficult terrain.</p>
                            <p>Low furniture, rubble, undergrowth, steep stairs, snow, and shallow bogs are examples of difficult terrain. The space of another creature, whether hostile or not, also counts as difficult terrain.</p>
                        </td>
                    </tr>
                    <tr>
                        <td>
                            <a class="badge badge-secondary" href="https://www.dndbeyond.com/sources/phb/combat#BeingProne" title="View description on D&amp;D Beyond" target="_BLANK">
                                Being Prone
                            </a>
                        </td>
                        <td>
                            <p>You can drop prone without using any of your speed. Standing up takes more effort; doing so costs an amount of movement equal to half your speed. For example, if your speed is 30 feet, you must spend 15 feet of movement to stand up.</p>
                            <p>You can't stand up if you don't have enough movement left or if your speed is 0. To move while prone, you must crawl or use magic such as teleportation.</p>
                            <p>Every foot of movement while crawling costs 1 extra foot. Crawling 1 foot in difficult terrain, therefore, costs 3 feet of movement.</p>
                        </td>
                    </tr>
                    <tr>
                        <td>
                            <a class="badge badge-secondary" href="https://www.dndbeyond.com/sources/phb/combat#MovingAroundOtherCreatures" title="View description on D&amp;D Beyond" target="_BLANK">
                                Moving Around Creatures
                            </a>
                        </td>
                        <td>
                            <p>You can move through a nonhostile creature's space. In contrast, you can move through a hostile creature's space only if the creature is at least two sizes larger or smaller than you.</p>
                            <p>Remember that another creature's space is difficult terrain for you. Whether a creature is a friend or an enemy, you can't willingly end your move in its space. If you leave a hostile creature's reach during your move, you provoke an opportunity attack, as explained later in the chapter.</p>
                        </td>
                    </tr>
                    <tr>
                        <td>
                            <a class="badge badge-secondary" href="https://www.dndbeyond.com/sources/phb/combat#FlyingMovement" title="View description on D&amp;D Beyond" target="_BLANK">
                                Flying
                            </a>
                        </td>
                        <td>
                            <p>Flying creatures enjoy many benefits of mobility, but they must also deal with the danger of falling. If a flying creature is knocked prone, has its speed reduced to 0, or is otherwise deprived of the ability to move, the creature falls, unless it has the ability to hover or it is being held aloft by magic, such as by the fly spell.</p>
                        </td>
                    </tr>
                    <tr>
                        <td>
                            <a class="badge badge-secondary" href="https://www.dndbeyond.com/sources/phb/adventuring#ClimbingSwimmingandCrawling" title="View description on D&amp;D Beyond" target="_BLANK">
                                Climbing/Swimming/Crawling
                            </a>
                        </td>
                        <td>
                            <p>Each foot of movement costs 1 extra foot (2 extra feet in difficult terrain) when you’re climbing, swimming, or crawling. You ignore this extra cost if you have a climbing speed and use it to climb, or a swimming speed and use it to swim.</p>
                            <p>At the DM’s option, climbing a slippery vertical surface or one with few handholds requires a successful Strength (Athletics) check. Similarly, gaining any distance in rough water might require a successful Strength (Athletics) check.</p>
                        </td>
                    </tr>
                    <tr>
                        <td>
                            <a class="badge badge-secondary" href="https://www.dndbeyond.com/sources/phb/adventuring#Jumping" title="View description on D&amp;D Beyond" target="_BLANK">
                                Jumping
                            </a>
                        </td>
                        <td>
                            <p>Your Strength determines how far you can jump.</p>

                            <p class="font-italic">Long Jump</h3>

                            <p>When you make a long jump, you cover a number of feet up to your Strength score if you move at least 10 feet on foot immediately before the jump. When you make a standing long jump, you can leap only half that distance. Either way, each foot you clear on the jump costs a foot of movement.</p>
                            <p>This rule assumes that the height of your jump doesn't matter, such as a jump across a stream or chasm.</p>
                            <p>At your DM's option, you must succeed on a DC 10 Strength (Athletics) check to clear a low obstacle (no taller than a quarter of the jump's distance), such as a hedge or low wall. Otherwise, you hit it.</p>
                            <p>When you land in difficult terrain, you must succeed on a DC 10 Dexterity (Acrobatics) check to land on your feet. Otherwise, you land prone.</p>

                            <p class="font-italic">High Jump</h3>

                            <p>When you make a high jump, you leap into the air a number of feet equal to 3 + your Strength modifier (minimum of 0 feet) if you move at least 10 feet on foot immediately before the jump. When you make a standing high jump, you can jump only half that distance. Either way, each foot you clear on the jump costs a foot of movement.</p>
                            <p>In some circumstances, your DM might allow you to make a Strength (Athletics) check to jump higher than you normally can.</p>
                            <p>You can extend your arms half your height above yourself during the jump. Thus, you can reach above you a distance equal to the height of the jump plus 1 1/2 times your height.</p>
                        </td>
