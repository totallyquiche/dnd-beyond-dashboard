                        <td>
                            <a class="badge badge-secondary" href="https://www.dndbeyond.com/sources/phb/combat#CastaSpell" title="View description on D&amp;D Beyond" target="_BLANK">
                                Cast a Spell
                            </a>
                        </td>
                        <td>
                            <p>Spellcasters such as wizards and clerics, as well as many monsters, have access to spells and can use them to great effect in combat.</p>
                            <p>Each spell has a casting time, which specifies whether the caster must use an action, a reaction, minutes, or even hours to cast the spell. Casting a spell is, therefore, not necessarily an action.</p>
                            <p>Most spells do have a casting time of 1 action, so a spellcaster often uses his or her action in combat to cast such a spell. See chapter 10 for the rules on spellcasting.</p>
                        </td>
                    </tr>
                    <tr>
                        <td>
                            <a class="badge badge-secondary" href="https://www.dndbeyond.com/sources/phb/combat#Dash" title="View description on D&amp;D Beyond" target="_BLANK">
                                Dash
                            </a>
                        </td>
                        <td>
                            <p>When you take the Dash action, you gain extra movement for the current turn. The increase equals your speed, after applying any modifiers. With a speed of 30 feet, for example, you can move up to 60 feet on your turn if you dash.</p>
                            <p>Any increase or decrease to your speed changes this additional movement by the same amount. If your speed of 30 feet is reduced to 15 feet, for instance, you can move up to 30 feet this turn if you dash.</p>
                        </td>
                    </tr>
                    <tr>
                        <td>
                            <a class="badge badge-secondary" href="https://www.dndbeyond.com/sources/phb/combat#Disengage" title="View description on D&amp;D Beyond" target="_BLANK">
                                Disengage
                            </a>
                        </td>
                        <td>
                            <p>If you take the Disengage action, your movement doesn't provoke opportunity attacks for the rest of the turn.</p>
                        </td>
                    </tr>
                    <tr>
                        <td>
                            <a class="badge badge-secondary" href="https://www.dndbeyond.com/sources/phb/combat#Dodge" title="View description on D&amp;D Beyond" target="_BLANK">
                                Dodge
                            </a>
                        </td>
                        <td>
                            When you take the Dodge action, you focus entirely on avoiding attacks. Until the start of your next turn, any attack roll made against you has disadvantage if you can see the attacker, and you make Dexterity saving throws with advantage. You lose this benefit if you are incapacitated or if your speed drops to 0.
                        </td>
                    </tr>
                    <tr>
                        <td>
                            <a class="badge badge-secondary" href="https://www.dndbeyond.com/sources/phb/combat#Help" title="View description on D&amp;D Beyond" target="_BLANK">
                                Help
                            </a>
                        </td>
                        <td>
                            <p>You can lend your aid to another creature in the completion of a task. When you take the Help action, the creature you aid gains advantage on the next ability check it makes to perform the task you are helping with, provided that it makes the check before the start of your next turn.</p>
                            <p>Alternatively, you can aid a friendly creature in attacking a creature within 5 feet of you. You feint, distract the target, or in some other way team up to make your ally's attack more effective.</p>
                            <p>If your ally attacks the target before your next turn, the first attack roll is made with advantage.</p>
                        </td>
                    </tr>
                    <tr>
                        <td>
                            <a class="badge badge-secondary" href="https://www.dndbeyond.com/sources/phb/combat#Hide" title="View description on D&amp;D Beyond" target="_BLANK">
                                Hide
                            </a>
                        </td>
                        <td>
                            <p>When you take the Hide action, you make a Dexterity (Stealth) check in an attempt to hide, following the rules for hiding. If you succeed, you gain certain benefits, as described in the "Unseen Attackers and Targets" section later in this chapter.</p>
                        </td>
                    </tr>
                    <tr>
                        <td>
                            <a class="badge badge-secondary" href="https://www.dndbeyond.com/sources/phb/combat#Ready" title="View description on D&amp;D Beyond" target="_BLANK">
                                Ready
                            </a>
                        </td>
                        <td>
                            <p>Sometimes you want to get the jump on a foe or wait for a particular circumstance before you act. To do so, you can take the Ready action on your turn, which lets you act using your reaction before the start of your next turn.</p>
                            <p>First, you decide what perceivable circumstance will trigger your reaction. Then, you choose the action you will take in response to that trigger, or you choose to move up to your speed in response to it.</p>
                            <p>Examples include "If the cultist steps on the trapdoor, I'll pull the lever that opens it," and "If the goblin steps next to me, I move away."</p>
                            <p>When the trigger occurs, you can either take your reaction right after the trigger finishes or ignore the trigger.</p>
                            <p>When you ready a spell, you cast it as normal but hold its energy, which you release with your reaction when the trigger occurs. To be readied, a spell must have a casting time of 1 action, and holding onto the spell's magic requires concentration (explained in chapter 10).</p>
                            <p>If your concentration is broken, the spell dissipates without taking effect. For example, if you are concentrating on the web spell and ready magic missile, your web spell ends, and if you take damage before you release magic missile with your reaction, your concentration might be broken.</p>
                        </td>
                    </tr>
                    <tr>
                        <td>
                            <a class="badge badge-secondary" href="https://www.dndbeyond.com/sources/phb/combat#Search" title="View description on D&amp;D Beyond" target="_BLANK">
                                Search
                            </a>
                        </td>
                        <td>
                            When you take the Search action, you devote your attention to finding something. Depending on the nature of your search, the DM might have you make a Wisdom (Perception) check or an Intelligence (Investigation) check.
                        </td>
                    </tr>
                    <tr>
                        <td>
                            <a class="badge badge-secondary" href="https://www.dndbeyond.com/sources/phb/combat#UseanObject" title="View description on D&amp;D Beyond" target="_BLANK">
                                Use an Object
                            </a>
                        </td>
                        <td>
                            You normally interact with an object while doing something else, such as when you draw a sword as part of an attack. When an object requires your action for its use, you take the Use an Object action. This action is also useful when you want to interact with more than one object on your turn.
                        </td>
