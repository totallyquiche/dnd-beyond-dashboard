                                Attack
                            </a>
                        </td>
                        <td>
                            <p>The most common action to take in combat is the Attack action, whether you are swinging a sword, firing an arrow from a bow, or brawling with your fists.</p>
                            <p>With this action, you make one melee or ranged attack. See the "Making an Attack" section for the rules that govern attacks.</p>
                            <p>Certain features, such as the Extra Attack feature of the fighter, allow you to make more than one attack with this action.</p>
                        </td>
